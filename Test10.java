public class Test10 {
    public static void main(String[] args) {
        byte num1 = 10;
        System.out.println("整数类型-byte:\n"+10);
        short num2 = 100;
        System.out.println("整数类型-short:\n"+100);
        int num3 = 1000;
        System.out.println("整数类型-int:\n"+1000);
        long num4 = 10000;
        System.out.println("整数类型-long:\n"+10000);
        float num5 = 100000.0f;
        System.out.println("小数类型-float:\n"+100000.0);
        double num6 = 1000000.0;
        System.out.println("小数类型-double:\n"+1000000.0);
        char num7 = 'Z';
        System.out.println("字符类型-char:\n"+num7);
        boolean num8 = false;
        System.out.println("布尔类型-boolean:\n"+num8);
    }
}
